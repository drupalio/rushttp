/// The HTTP Protcol
#[derive(Debug, PartialEq, Copy, Clone)]
pub enum Protocol {
    Http10,
    Http11,
}

/// The HTTP Method.
/// Every HTTP request has a method - GET is the most comment.
#[derive(Debug, PartialEq, Copy, Clone)]
pub enum Method {
    Options,
    Get,
    Post,
    Put,
    Delete,
    Head,
    Trace,
    Connect,
    Patch,
}

